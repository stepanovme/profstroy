<?php
// Проверяем, был ли отправлен POST-запрос
if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Проверяем, существует ли переменная host в данных POST-запроса
    if(isset($_POST['host'])) {
        // Получаем путь к файлу из данных POST-запроса
        $host = $_POST['host'];

        // Здесь вы можете выполнить необходимые операции с путем к файлу,
        // например, сохранить его в переменную, файл или базу данных

        // Пример: сохраняем путь к файлу в переменную
        $saved_host = $host;

        // Возвращаем ответ клиенту
        echo "Путь к файлу сохранен: " . $saved_host;
    } else {
        // Если переменная host отсутствует в данных POST-запроса
        echo "Переменная host не найдена в запросе";
    }
} else {
    // Если запрос не является POST-запросом
    echo "Этот скрипт ожидает POST-запроса";
}

echo $host;
?>
